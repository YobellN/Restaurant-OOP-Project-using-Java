    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        
            
            // SET ATRIBUT DAN METODE PEMBAYARAN
            int counter = 0;
            String checkbox = "";
            String radio = "";
            
            // MENCARI INDEX YANG DITUNJUK OLEH DROPDOWN
            // CUSTOMER
            int selectedIndexSingle = namaData1Dropdown.getSelectedIndex();            
            Penitip selectedSingle = singleList.get(selectedIndexSingle);
            
            // KENDARAAN
            int selectedIndexMulti = namaData2Dropdown.getSelectedIndex();
            Produk selectedMulti = multiList.get(selectedIndexMulti);
            
            if(checkboxData1.isSelected() && checkboxData2.isSelected() && checkboxData3.isSelected()){
                checkbox = "Berhati-hati dengan produk ini!";
            }else{
                if(checkboxData1.isSelected()){
                    checkbox += "Simpan pada suhu ruang";
                    counter++;                    
                }
                
                if(checkboxData2.isSelected()){
                    if(counter == 0){
                        checkbox += "Jangan diguncang";
                    }else{
                        checkbox += ", jangan diguncang";
                    }
                    counter++;
                }
                
                if(checkboxData3.isSelected()){
                    if(counter == 0){
                        checkbox += "Tidak boleh dibalik";
                    }else{
                        checkbox += ", tidak boleh dibalik";
                    }
                }
            }
            
            radio = radioButtonGroup.getSelection().getActionCommand();
            
            if(action.equals("tambah")){
                Penitipan dataNew = new Penitipan(selectedSingle.getId_penitip(), selectedMulti.getId_produk(), checkbox, 
                    radio, dataInput1.getText(), Integer.parseInt(dataInput2.getText()), selectedSingle, selectedMulti);
                junctionControl.insertData(dataNew);
            } else {
                Penitipan dataNew = new Penitipan(selectedSingle.getId_penitip(), selectedMulti.getId_produk(), checkbox, 
                    radio, dataInput1.getText(), Integer.parseInt(dataInput2.getText()), selectedSingle, selectedMulti);
                junctionControl.updateData(dataNew, selectedId);
                selectedId = -1;
            }
        
                
        clearText();
        setComponent(false);
        setEditDeleteBtn(false);
        setRadioComponent(false);
        radioButtonGroup.clearSelection();
        tambahDataButton.setEnabled(true);
        showTableBySearch("");
    }//GEN-LAST:event_saveButtonActionPerformed

