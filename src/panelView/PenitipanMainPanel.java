    private void tableDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDataMouseClicked
        int indexSingle = -1, indexMulti = -1;
        action = "baharui";
        
        tambahDataButton.setEnabled(false);
        cancelButton.setEnabled(true);
        saveButton.setEnabled(true);        
        setEditDeleteBtn(true);
        
        setComponent(false);
        setRadioComponent(false);        
        radioButtonGroup.clearSelection();
        
        int clickedRow = tableData.getSelectedRow();
        TableModel tableModel = tableData.getModel();
        
        selectedId = Integer.parseInt(tableModel.getValueAt(clickedRow, 9).toString());

        dataInput1.setText(tableModel.getValueAt(clickedRow, 6).toString());
        dataInput2.setText(tableModel.getValueAt(clickedRow, 5).toString());
        
        String checkbox = tableModel.getValueAt(clickedRow, 3).toString();                
        String radio = tableModel.getValueAt(clickedRow, 4).toString();

        switch(radio){
            case "Pending" :
                radioData1.setSelected(true);
                break;
            case "In Progress" :
                radioData2.setSelected(true);
                break;
            case "Completed":
                radioData3.setSelected(true);
                break;
        }
        
        checkboxData1.setSelected(false);
        checkboxData2.setSelected(false);
        checkboxData3.setSelected(false);
        
        switch(checkbox){
            case "Simpan pada suhu ruang" :
                checkboxData1.setSelected(true);
                break;
            case "Jangan diguncang" :
                checkboxData2.setSelected(true);
                break;
            case "Tidak boleh dibalik" :
                checkboxData3.setSelected(true);
                break;
            case "Berhati-hati dengan produk ini!" :
                checkboxData1.setSelected(true);
                checkboxData2.setSelected(true);
                checkboxData3.setSelected(true);
                break;
            case "Simpan pada suhu ruang, jangan diguncang" :
                checkboxData1.setSelected(true);
                checkboxData2.setSelected(true);
                break;
            case "Simpan pada suhu ruang, tidak boleh dibalik" :
                checkboxData1.setSelected(true);
                checkboxData3.setSelected(true);
                break;
            case "Jangan diguncang, tidak boleh dibalik" :
                checkboxData2.setSelected(true);
                checkboxData3.setSelected(true);
                break;
        }

        for(Penitip first : singleList){
            if(first.getId_penitip() == Integer.parseInt(tableModel.getValueAt(clickedRow, 7).toString())){
                indexSingle = singleList.indexOf(first);
            }
        }
        
        for(Produk second : multiList){
            if(second.getId_produk().equals(tableModel.getValueAt(clickedRow, 8).toString())){
                indexMulti = multiList.indexOf(second);
            }
        }        
        
        namaData1Dropdown.setSelectedIndex(indexSingle);
        namaData2Dropdown.setSelectedIndex(indexMulti);
        cancelButton.setEnabled(true);
    }//GEN-LAST:event_tableDataMouseClicked

