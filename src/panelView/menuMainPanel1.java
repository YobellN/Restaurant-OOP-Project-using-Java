    private void doSearchKendaraan(){
        if(searchPesananInputTextField.getText().isEmpty())
            return;
        
            k = kc.searchDataKendaraan(searchPesananInputTextField.getText());
            
        if(k != null){
            setKendaraanEditDeleteButton(true);
            idMenuInputTextField.setText(k.getId_kendaraan());
            namaMenuInputTextField.setText(k.getNama());
            hargaMenuInputTextfield.setText(Float.toString(k.getHarga()));
            jenisInputButton.setText(k.getJenis());
            setSpecialAtributeLabel();
            specialAtributeInputTextfield.setText(k.getSpecial());
        }else{
            JOptionPane.showMessageDialog(rootPane, "NOT FOUND !!!");
        }
    }

    private void setSpecialAtributeLabel(){
        if(jenisInputButton.getText().equals("Mobil")){
            specialAtributeInputLabel.setText("Jenis Mesin");
        }else{
            specialAtributeInputLabel.setText("Jumlah Tak");
        }
    }
    
