    private void simpanKendaraanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanKendaraanButtonActionPerformed
        if(actionKendaraan == null)
            return;
        try{
            inputKosongKendaraanException();
            InputSpecialAtributeKendaraanException();
            
            if(JOptionPane.showConfirmDialog(rootPane, "yakin ingin melakukan " + actionKendaraan + "?") == JOptionPane.CLOSED_OPTION)
                return;

            switch (actionKendaraan){
                case "add":
                    if(mobilIsSelected()){
                        mbl = new Mobil(specialAtributeInputTextfield.getText(), namaMenuInputTextField.getText(), 
                                jenisInputButton.getText(), Float.parseFloat(hargaMenuInputTextfield.getText()));
                        mblc.insertDataKendaraan(mbl);
                    }else{
                        mtr = new Motor(Integer.parseInt(specialAtributeInputTextfield.getText()), namaMenuInputTextField.getText(), 
                                jenisInputButton.getText(), Float.parseFloat(hargaMenuInputTextfield.getText()));
                        mtrc.insertDataKendaraan(mtr);
                    }
                    clearText();
                    setKendaraanEditDeleteButton(false);
                    setComponentsKendaraan(false);
                    showKendaraan();
                    break;
                case "update":
                    if(mobilIsSelected()){
                        mbl = new Mobil(specialAtributeInputTextfield.getText(),idMenuInputTextField.getText(), namaMenuInputTextField.getText(), 
                                jenisInputButton.getText(), Float.parseFloat(hargaMenuInputTextfield.getText()));
                        mblc.updateDataKendaraan(mbl);
                    }else{
                        mtr = new Motor(Integer.parseInt(specialAtributeInputTextfield.getText()),idMenuInputTextField.getText(), namaMenuInputTextField.getText(), 
                                jenisInputButton.getText(), Float.parseFloat(hargaMenuInputTextfield.getText()));
                        mtrc.updateDataKendaraan(mtr);
                    }
                    clearText();
                    setKendaraanEditDeleteButton(false);
                    setComponentsKendaraan(false);
                    showKendaraan();
                    break;
                default:
                    break;
            }
            actionKendaraan = null;
        }catch(InputKosongException e){
            JOptionPane.showMessageDialog(rootPane, e.message());
        }catch(InputSpecialAtributeException e){
            JOptionPane.showMessageDialog(rootPane, e.message(jenisInputButton.getText()));
        }
    }//GEN-LAST:event_simpanKendaraanButtonActionPerformed

