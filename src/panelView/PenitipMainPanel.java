    private void doSearchCustomer(){
        if(searchPenitipInputTextField.getText().isEmpty())
            return;
            Karyawan c = dataControl.searchCustomerById(Integer.parseInt(searchPenitipInputTextField.getText()));
            if(c == null ){
                JOptionPane.showMessageDialog(rootPane, "NOT FOUND !!!");
                return;
            }
            
            setEditDeleteButtonData(true);
            clearTextData();
            
            namaPenitipInputTextField.setText(c.getNama());
            nomorTeleponInputTextField.setText(c.getAlamat());
            alamatInputTextField.setText(c.getNo_telepon());
            
            idCustomer = c.getId_penitip();
    }

