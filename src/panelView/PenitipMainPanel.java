    private void simpanPenitipButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanPenitipButtonActionPerformed
        // TODO add your handling code here:
        int dialog = JOptionPane.showConfirmDialog(rootPane, "yakin ingin melakukan " + actionCustomer + "?");
        if(dialog == JOptionPane.CLOSED_OPTION || dialog == JOptionPane.NO_OPTION || dialog == JOptionPane.CANCEL_OPTION)
        return;
        
            if(JOptionPane.showConfirmDialog(rootPane, "yakin ingin melakukan " + actionCustomer + "?") == JOptionPane.CLOSED_OPTION)
            return;
            switch(actionCustomer){
                case "add":
                dataModel = new Karyawan(namaPenitipInputTextField.getText(), nomorTeleponInputTextField.getText(), alamatInputTextField.getText());
                dataControl.insertDataPenitip(dataModel);
                clearTextData();
                setEditDeleteButtonData(false);
                setComponentsData(false);
                break;
                
                case "update":
                dataModel = new Karyawan(namaPenitipInputTextField.getText(), nomorTeleponInputTextField.getText(), alamatInputTextField.getText());
                dataControl.updateDataCustomer(dataModel, idCustomer);
                clearTextData();
                setEditDeleteButtonData(false);
                setComponentsData(false);
                break;
                default:
                break;
            }
            showCustomer();
            actionCustomer = null;
    }//GEN-LAST:event_simpanPenitipButtonActionPerformed


