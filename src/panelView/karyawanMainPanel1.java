    private void simpanJabatanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanJabatanButtonActionPerformed
        // TODO add your handling code here:
        if(actionCustomer == null)
        return;
        try{
            inputKosongCustomerException();
            if(JOptionPane.showConfirmDialog(rootPane, "yakin ingin melakukan " + actionCustomer + "?") == JOptionPane.CLOSED_OPTION)
            return;
            
            switch(actionCustomer){
                case "add":
                c = new Customer(namaKaryawanInputTextField.getText(),  alamatInputTextField.getText(),gajiKaryawanInputTextField.getText());
                cc.insertDataCustomer(c);
                clearTextCustomer();
                setEditDeleteButtonCustomer(false);
                setComponentsCustomer(false);
                break;
                case "update":
                c = new Customer(namaKaryawanInputTextField.getText(), alamatInputTextField.getText() , gajiKaryawanInputTextField.getText());
                cc.updateDataCustomer(c, idCustomer);
                clearTextCustomer();
                setEditDeleteButtonCustomer(false);
                setComponentsCustomer(false);
                break;
                default:
                break;
            }
            showCustomer();
            actionCustomer = null;
        }catch(InputKosongException e){
            System.out.println(e);
        };
    }//GEN-LAST:event_simpanJabatanButtonActionPerformed

