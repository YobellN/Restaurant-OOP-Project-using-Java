    private void searchKaryawanInputTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchKaryawanInputTextFieldKeyTyped
          if(evt.getKeyChar()=='\n'){
          if(searchKaryawanInputTextField.getText().isEmpty())
              return;
          
          Customer c = cc.searchCostumerByName(searchKaryawanInputTextField.getText());
          setEditDeleteButtonCustomer(true);
          clearTextCustomer();
          
          namaKaryawanInputTextField.setText(c.getNama());
          gajiKaryawanInputTextField.setText(c.getAlamat());
          alamatInputTextField.setText(c.getNo_telepon());
          
          idCustomer = c.getId_customer();
          }
    }//GEN-LAST:event_searchKaryawanInputTextFieldKeyTyped

